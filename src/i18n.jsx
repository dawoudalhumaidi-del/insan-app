const LANGUAGES = [
  { code: 'ar', name: 'العربية',     flag: '🇸🇦', dir: 'rtl' },
  { code: 'en', name: 'English',     flag: '🇬🇧', dir: 'ltr' },
  { code: 'ur', name: 'اردو',         flag: '🇵🇰', dir: 'rtl' },
  { code: 'fr', name: 'Français',    flag: '🇫🇷', dir: 'ltr' },
];

const I18nCtx = React.createContext({ lang: 'ar', t: (k) => k, setLang: () => {}, dir: 'rtl' });

function I18nProvider({ children, initialLang = 'ar' }) {
  const [lang, setLang] = React.useState(initialLang);
  const t = React.useCallback((key) => {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.ar;
    return dict[key] ?? TRANSLATIONS.ar[key] ?? key;
  }, [lang]);
  const dir = (LANGUAGES.find(l => l.code === lang) || {}).dir || 'rtl';

  React.useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = lang;
  }, [lang, dir]);

  const value = React.useMemo(() => ({ lang, t, setLang, dir, languages: LANGUAGES }), [lang, t, dir]);
  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

window.I18nCtx = I18nCtx;
window.I18nProvider = I18nProvider;
window.LANGUAGES = LANGUAGES;
window.useT = () => React.useContext(I18nCtx);
